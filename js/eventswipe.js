// ----------- 이벤트 슬라이더 컨트롤
$(function () {
    $(".eventnav span").eq(0).click(function () {
        $(".eventnav span").removeClass('swiperbulletactive')
        $(this).addClass('swiperbulletactive');
        $("#swiperitemevent .swiperslide").css("display", "none");
        $("#swiperitemevent .swiperslide").eq(0).css("display", "block");
    })
})

