$(function () {
    $(".swiperbtn .sbtns").eq(0).mouseenter(function () {
        $(".swiperbtn .sbtns").css({
            "background-color" : "#e5eff7",
            "border-radius" : "none",
            "color" : "#3C5980"
        })
        $(this).css({
            "background-color" : "#2A68B9",
            "border-radius" : "17px",
            "color" : "#fff"
        });
        $(".swiperinner").css("display", "none");
        $(".swiperinner").eq(0).css("display", "flex");
    })
})

